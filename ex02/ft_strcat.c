int	ft_strcat(char *dest, char *src)
{
	int a;
	int b;

	a = 0;
	while (dest[a] != '\0')
	{
		a += 1;
	}
	b = 0;
	while (src[b] != '\0')
	{
		dest[a + b] = src[b];
		b += 1;
	}
	if (src[b] == '\0')
	{
		dest[a + b] = '\0';
	}
	return (dest);
}
